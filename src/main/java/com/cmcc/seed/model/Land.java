package com.cmcc.seed.model;

import java.io.Serializable;
import java.util.Date;

public class Land implements Serializable{

    private static final long serialVersionUID = -160013283506116940L;

    private Long id;

    private Long userId;

    private Integer num;

    private Long cropId;

    private Integer status;

    private Date plantTime;

    private Double expectYield;

    private Double yield;

    private Date lastWaterTime;

    private Date createTime;

    private Date updateTime;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public Integer getNum() {
        return num;
    }

    public void setNum(Integer num) {
        this.num = num;
    }

    public Long getCropId() {
        return cropId;
    }

    public void setCropId(Long cropId) {
        this.cropId = cropId;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Date getPlantTime() {
        return plantTime;
    }

    public void setPlantTime(Date plantTime) {
        this.plantTime = plantTime;
    }

    public Double getExpectYield() {
        return expectYield;
    }

    public void setExpectYield(Double expectYield) {
        this.expectYield = expectYield;
    }

    public Double getYield() {
        return yield;
    }

    public void setYield(Double yield) {
        this.yield = yield;
    }

    public Date getLastWaterTime() {
        return lastWaterTime;
    }

    public void setLastWaterTime(Date lastWaterTime) {
        this.lastWaterTime = lastWaterTime;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
}