package com.cmcc.seed.pojo;

import java.io.Serializable;

/**
 * Created by cmcc on 16/1/15.
 */
public class FriendPlantPojo implements Serializable {

    private static final long serialVersionUID = 2427403997342021969L;
    /**
     * 唯一标识ID
     */
    private long id;

    /**
     * 土地编号
     */
    private int ground;

    /**
     * 状态
     * 可浇水/偷取/正常
     * 0/1/-1
     */
    private int status;

    /**
     * 生长状态
     * 0、1、2、3、4、5
     */
    private int growthStatus;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public int getGround() {
        return ground;
    }

    public void setGround(int ground) {
        this.ground = ground;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getGrowthStatus() {
        return growthStatus;
    }

    public void setGrowthStatus(int growthStatus) {
        this.growthStatus = growthStatus;
    }

}
